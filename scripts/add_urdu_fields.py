def repl(match):
    attrs = match.group(1)
    order = re.search(r'order="([^"]+)"', attrs).group(1)
    data = urdu.get(order)
    if not data:
        return match.group(0)
    title_ur, desc_ur_title, desc_en, desc_ur = data
    attrs = re.sub(r' title="[^"]+"', lambda m: m.group(0) + f' titleUrdu="{title_ur}" description="{desc_en}" descriptionUrdu="{desc_ur}"', attrs, count=1)
    return f'<audio{attrs} />'

xml = re.sub(r'<audio([^>]+)/>', repl, xml)
xml = re.sub(r'<volume id="([^"]+)" label="([^"]+)"', lambda m: f'<volume id="{m.group(1)}" label="{m.group(2)}" labelUrdu="جلد {int(m.group(2).split()[-1]):02d}"', xml)
